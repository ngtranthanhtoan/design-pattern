interface DocumentStyle {
  fontFamily: string;
  fontSize: number;
  lineHeight: number;
  color: string;
  backgroundColor?: string;
  margin: {
    top: number;
    right: number;
    bottom: number;
    left: number;
  };
}

interface DocumentHeader {
  title: string;
  subtitle?: string;
  logo?: string;
  metadata: {
    author: string;
    company: string;
    date: Date;
    version: string;
  };
}

interface DocumentSection {
  id: string;
  title: string;
  content: string;
  subsections: DocumentSubsection[];
  style?: Partial<DocumentStyle>;
}

interface DocumentSubsection {
  id: string;
  title: string;
  content: string;
  type: 'text' | 'table' | 'chart' | 'image' | 'list';
  data?: any;
}

interface DocumentFooter {
  content: string;
  pageNumbers: boolean;
  contactInfo?: string;
  disclaimer?: string;
}

interface TemplateConfiguration {
  type: 'invoice' | 'report' | 'contract' | 'email' | 'presentation';
  category: string;
  version: string;
  language: string;
  orientation: 'portrait' | 'landscape';
  pageSize: 'A4' | 'A3' | 'Letter' | 'Legal';
  encryption?: boolean;
  watermark?: string;
}

